package Modelo;

/**
 *
 * @author PC
 */
public class User {
    String idUser;
    String name;
    String emailAddress;
    String password;
    int age;
    String type;

    public String getIdUser() {
        return idUser;
    }

    public void setIdUser(String idUser) {
        this.idUser = idUser;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmailAddress() {
        return emailAddress;
    }

    public void setEmailAddress(String emailAddress) {
        this.emailAddress = emailAddress;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public User(String idUser, String name, String emailAddress, String password, int age, String type) {
        this.idUser = idUser;
        this.name = name;
        this.emailAddress = emailAddress;
        this.password = password;
        this.age = age;
        this.type = type;
    }
    
    
    
    
    
    
}
